public class ForceCatalyst extends Spell {
    {
        image = ItemSpriteSheet.FORCE_CATALYST;
    }

    @Override
    protected void onCast(Hero hero) {
        updateQuickslot();
    }

    @Override
    public int value() {
        return 40 * quantity;
    }

    public static class Recipe extends com.shatteredpixel.shatteredpixeldungeon.items.Recipe {

        @Override
        public boolean testIngredients(ArrayList<Item> ingredients) {
            boolean weapon = false;
            boolean Arcane = false;
            for (Item i : ingredients){
                if (i instanceof MeleeWeapon){
                    weapon = true;
                    //if it is a regular or exotic potion
                } else if (i instanceof ArcaneCatalyst) {
                    Arcane = true;
                }
            }

            return weapon && Arcane;
        }

        @Override
        public int cost(ArrayList<Item> ingredients) {
            return 1;
        }

        @Override
        public Item brew(ArrayList<Item> ingredients) {
            if (!testIngredients(ingredients)) return null;

            for (Item ingredient : ingredients){
                ingredient.quantity(ingredient.quantity() - 1);
            }

            return sampleOutput(null);
        }

        @Override
        public Item sampleOutput(ArrayList<Item> ingredients) {
            return new ForceCatalyst();
        }
    }
}
